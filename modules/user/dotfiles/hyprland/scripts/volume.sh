#!/usr/bin/env bash
# =============================================================================
# volume.sh — обёртка над wpctl с двумя гарантиями:
#   1. громкость не поднимается выше 100% (лимит на софт-усилении)
#   2. при опускании до 0 — автоматически включается mute,
#      и waybar показывает иконку перечёркнутого динамика
# =============================================================================
# Использование:
#   volume.sh up    — громче на 5%, не выше 100%, размьютить если был мут
#   volume.sh down  — тише на 5%, замьютить если упало до 0
#   volume.sh mute  — переключить mute
# =============================================================================
set -euo pipefail

SINK="@DEFAULT_AUDIO_SINK@"
STEP="5%"

case "${1:-}" in
  up)
    # Если был замьючен — снять мут, иначе wpctl поднимет громкость замьюченного
    wpctl set-mute "$SINK" 0
    # Поднять с ограничением 100% (-l 1.0 = limit 100%)
    wpctl set-volume -l 1.0 "$SINK" "${STEP}+"
    ;;

  down)
    # Опустить громкость
    wpctl set-volume "$SINK" "${STEP}-"
    # Проверить — не упало ли до 0
    # Вывод wpctl get-volume: "Volume: 0.45" или "Volume: 0.00 [MUTED]"
    vol=$(wpctl get-volume "$SINK" | awk '{print $2}')
    if [[ "$vol" == "0.00" ]]; then
      wpctl set-mute "$SINK" 1
    fi
    ;;

  mute)
    wpctl set-mute "$SINK" toggle
    ;;

  *)
    echo "Usage: $0 {up|down|mute}" >&2
    exit 1
    ;;
esac
